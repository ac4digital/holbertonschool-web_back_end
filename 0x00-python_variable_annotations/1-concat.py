#!/usr/bin/env python3
""" Script that concatenates two strings and 
    returns a new string """


def concat(str1: str, str2: str) -> str:
    """ Concatenates two strings to create a new string
    Args:
        str1: string to concat
        str2: string to concat
    Returns: string, new string concatened by str1 and str2
    """
    return str1 + str2
